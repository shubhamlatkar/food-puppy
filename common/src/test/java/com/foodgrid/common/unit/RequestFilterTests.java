@SpringBootTest
@AutoConfigureWebTestClient
@Slf4j
class RequestFilterTests {

    @MockBean
    private JwtTokenUtility jwtTokenUtility;

    @MockBean
    private UserDetailsServiceImplementation userDetailsService;

    @MockBean
    private UserRepository userRepository;

    @MockBean
    private UserSession userSession;

    @MockBean
    private HttpServletRequest httpServletRequest;

    @MockBean
    private HttpServletResponse httpServletResponse;

    @MockBean
    private FilterChain chain;

    @MockBean
    private RoleRepository roleRepository;

    @BeforeEach
    void init() {
        var tempRole = new Role("USER", List.of(new Authority("1", "TEST_AUTH")));
        when(roleRepository.findByName("USER")).thenReturn(java.util.Optional.of(tempRole));
    }

    @Test
    void testBeanConfigurationTestsPasswordEncoder() {
        var tempRole = new Role("USER", List.of(new Authority("1", "TEST_AUTH")));
        var tempUser = new User("test_username", "1234567890", "testemail@email.com", "test_pass", List.of(tempRole), UserTypes.USER);
        tempUser.setMetadata(new UserMetadata(new Date(), new Date(), UserActivities.LOGIN));
        tempUser.setId("1");
        tempUser.addToken("test_token");

        var testRequestFilter = new TestRequestFilter(jwtTokenUtility, userDetailsService, userRepository, userSession);

        doAnswer(invocationOnMock -> java.util.Optional.of(tempUser))
                .when(userRepository).findByUsername(anyString());

        doAnswer(invocationOnMock -> "test_username")
                .when(jwtTokenUtility).getUsernameFromToken(anyString());

        doAnswer(invocationOnMock -> {
            Set<GrantedAuthority> authorities = tempUser.getRoles().stream()
                    .map(role -> new SimpleGrantedAuthority("ROLE_" + role.getName()))
                    .collect(Collectors.toSet());

            tempUser.getRoles().forEach(role -> {
                for (Authority authority : role.getAuthorities()) {
                    authorities.add(new SimpleGrantedAuthority(authority.getName()));
                }
            });

            return new UserDetailsImplementation(
                    tempUser.getUsername(),
                    tempUser.getPassword(),
                    authorities,
                    tempUser.getEmail(),
                    tempUser.getId()
            );
        })
                .when(userDetailsService).loadUserByUsername(anyString());

        try {
            testRequestFilter.doFilterInternal(new TestHttpRequest(), new TestHttpResponse(), new TestFilterChain());
        } catch (IOException | ServletException ioException) {
            log.info("Io Exception");
        }
        Assertions.assertNotNull(roleRepository.findByName("USER"));
    }
}

